/*
 * dmst.h
 *
 * Заголовочный файл с интерфейсом для поиска ориентированного
 * минимального остовного дерева (minimum spanning arborescence)
 * в ориентированном графе.
 *
 * В проекте реализованы три версии алгоритма:
 *
 * 1) dmst_naive()
 *    Простая реализация алгоритма Чу–Лю/Эдмондса.
 *    Работает за O(V * E), где:
 *      V — число вершин,
 *      E — число рёбер.
 *
 * 2) dmst_tarjan()
 *    Более эффективная реализация на основе алгоритма Тарьяна.
 *    Использует дополнительные структуры данных и имеет
 *    асимптотику O(E log V) для разреженных графов.
 *
 * 3) dmst_gabow()
 *    Реализация алгоритма
 *    Габова–Галиля–Спенсера–Тарьяна.
 *    Использует тот же интерфейс вызова.
 *
 * Все функции принимают ориентированный граф,
 * корневую вершину и возвращают:
 *   - массив родителей вершин,
 *   - суммарный вес построенного дерева.
 *
 * Массив parent_out индексируется по вершинам:
 * parent_out[v] хранит вершину-предка для v
 * на пути к корню.
 *
 * Для корневой вершины:
 * parent_out[root] = -1.
 *
 * Если построить ориентированное остовное дерево нельзя
 * (например, корень не достижим для части графа),
 * функция возвращает отрицательное значение.
 */

#ifndef DMST_H
#define DMST_H

#include <stddef.h>

/*
 * Описание ориентированного взвешенного ребра.
 *
 * Вершины задаются индексами от 0.
 *
 * Поле id используется внутри алгоритмов,
 * чтобы отслеживать исходное ребро при
 * свёртке циклов и рекурсивных преобразованиях.
 * Пользователю библиотеки это поле обычно
 * не требуется.
 */
typedef struct {
    int src;      /* вершина-источник */
    int dst;      /* вершина-приёмник */
    long weight;  /* вес ребра */
    int id;       /* внутренний идентификатор */
} dmst_edge_t;

/*
 * Представление ориентированного графа.
 *
 * Граф хранит:
 *   - количество вершин,
 *   - количество рёбер,
 *   - массив рёбер.
 *
 * Вершины нумеруются от 0 до n - 1.
 *
 * Выделение и освобождение памяти
 * для массива edges выполняется
 * вызывающей стороной.
 */
typedef struct {
    int n;              /* количество вершин */
    int m;              /* количество рёбер */
    dmst_edge_t *edges; /* массив рёбер */
} dmst_graph_t;

/*
 * Поиск минимального ориентированного остовного дерева
 * с помощью алгоритма Чу–Лю/Эдмондса.
 *
 * Параметры:
 *   g
 *      Указатель на граф.
 *      Во время работы функции граф не изменяется.
 *
 *   root
 *      Индекс корневой вершины.
 *      Должен находиться в диапазоне:
 *      0 <= root < g->n.
 *
 *   parent_out
 *      Массив длиной g->n,
 *      в который записываются родители вершин.
 *
 *      После успешного выполнения:
 *      parent_out[v] содержит родителя вершины v.
 *
 *      Для корня:
 *      parent_out[root] = -1.
 *
 *   weight_out
 *      Переменная для записи суммарного
 *      веса найденного дерева.
 *
 * Возвращаемое значение:
 *   0  — дерево успешно найдено.
 *  -1  — построение невозможно.
 */
int dmst_naive(
    const dmst_graph_t *g,
    int root,
    int *parent_out,
    long *weight_out
);

/*
 * Поиск минимального ориентированного остовного дерева
 * с использованием алгоритма Тарьяна.
 *
 * Интерфейс полностью совместим с dmst_naive().
 * Функция принимает те же параметры
 * и возвращает результат в том же формате.
 */
int dmst_tarjan(
    const dmst_graph_t *g,
    int root,
    int *parent_out,
    long *weight_out
);

/*
 * Поиск минимального ориентированного остовного дерева
 * алгоритмом Габова–Галиля–Спенсера–Тарьяна.
 *
 * Интерфейс совпадает с dmst_naive()
 * и dmst_tarjan().
 */
int dmst_gabow(
    const dmst_graph_t *g,
    int root,
    int *parent_out,
    long *weight_out
);

#endif /* DMST_H */